using System;

namespace _01.NumbersFrom1to100
{
    internal class Program
    {
        //Напишете програма, която отпечатва числата от 1 до 100, всяко на нов ред
        static void Main(string[] args)
        {
            for (int i = 1; i <= 100; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
